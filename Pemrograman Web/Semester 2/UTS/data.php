class BMI {
    public $berat;
    public $tinggi;
    public $bmi;
    public $status;

    public function __construct (Int $berat, Int $tinggi) {
        $this->berat = $berat;
        $this->tinggi = $tinggi;
        $this->bmi = $berat / $tinggi**2;
        
        if ($this->bmi < 18.5) {
            $this->status = "Kekurangan berat badan.";
        } else if ($this->bmi < 24.9) {
            $this->status = "Normal (ideal)";
        } else if ($this->bmi < 29.9) {
            $this->status = "Kelebihan berat badan";
        } else {
            $this->status = "Kegemukan a.k.a Obesitas";
        }
    }

    public function nilaiBMI() {
        return $this->bmi;
    }

    public function statusBMI() {
        return $this->status;
    }
}

